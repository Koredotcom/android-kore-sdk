    public static int getColorForFileType(String fileType) {
        if (fileType == null) return Color.parseColor(COLOR_DOCX);
        if (fileType.equalsIgnoreCase(EXT_PDF) || fileType.equalsIgnoreCase(EXT_PPT) || fileType.equalsIgnoreCase(EXT_PPTX))
            return Color.parseColor(COLOR_PDF_OR_PPTX);
        else if (fileType.equalsIgnoreCase(EXT_TXT) || fileType.equalsIgnoreCase(EXT_GIF) || fileType.equalsIgnoreCase(EXT_ICO))
            return Color.parseColor(COLOR_GIF);
        else if (fileType.equalsIgnoreCase(EXT_PAGES) || fileType.equalsIgnoreCase(EXT_RTF) || fileType.equalsIgnoreCase(EXT_WPD))
            return Color.parseColor(COLOR_WPS);
        else if (fileType.equalsIgnoreCase(EXT_DOC) || fileType.equalsIgnoreCase(EXT_DOCX) || fileType.equalsIgnoreCase(EXT_JPG) || fileType.equalsIgnoreCase(EXT_JPEG)
                || fileType.equalsIgnoreCase(EXT_MPG)) return Color.parseColor(COLOR_DOCX);
        else if (fileType.equalsIgnoreCase(EXT_XLS) || fileType.equalsIgnoreCase(EXT_XLSX) || fileType.equalsIgnoreCase(EXT_GSHEET))
            return Color.parseColor(COLOR_XLS);
        else if (fileType.equalsIgnoreCase(EXT_MP3) || fileType.equalsIgnoreCase(EXT_WAV) || fileType.equalsIgnoreCase(EXT_MP4))
            return Color.parseColor(COLOR_AUDIO);
        else if (fileType.equalsIgnoreCase(EXT_SKETCH) || fileType.equalsIgnoreCase("crdownload"))
            return Color.parseColor(COLOR_SKETCH);
        else if (fileType.equalsIgnoreCase(EXT_TXT) || fileType.equalsIgnoreCase(EXT_HTML) || fileType.equalsIgnoreCase(EXT_ZIP) || fileType.equalsIgnoreCase(EXT_7ZIP)
                || fileType.equalsIgnoreCase(EXT_JAR) || fileType.equalsIgnoreCase("js") || fileType.equalsIgnoreCase("java")
                || fileType.equalsIgnoreCase(EXT_SQL)) return Color.parseColor(COLOR_TXT);
        else return Color.parseColor(COLOR_DOCX);
    }

    public static int getDrawableByExt(String ext){
        if(videoTypes.contains(ext)){
            return R.drawable.ic_video;
        }
        else if(msDoc.contains(ext))
        {
            return R.drawable.ic_document;
        }
        else if(msPowerpaint.contains(ext))
        {
            return R.drawable.ic_slides;
        }
        else if(msExcel.contains(ext))
        {
            return R.drawable.ic_sheet;
        }
        else if(audioTypes.contains(ext)){
            return R.drawable.ic_music;
        }else if(spreadTypes.contains(ext)){
            return R.drawable.ic_sheet_old;
        }else if(docTypes.contains(ext)){
            return R.drawable.ic_document_old;
        }else if(threeDImgTypes.contains(ext)){
            return R.drawable.ic_3d_object;
        }else if(rasterImageTypes.contains(ext)){
            return R.drawable.ic_raster_image;
        }else if(vectorImgTypes.contains(ext)){
            return R.drawable.ic_vector_type;
        }else if(dbTypes.contains(ext)){
            return R.drawable.ic_database_type;
        }else if(devTypes.contains(ext)){
            return R.drawable.ic_developer;
        }else if(exeTypes.contains(ext)){
            return R.drawable.ic_executable;
        }else if(systemTypes.contains(ext)){
            return R.drawable.ic_settings;
        }else if(compressedTypes.contains(ext)){
            return R.drawable.ic_zip;
        }else if(presentationTypes.contains(ext)){
            return R.drawable.ic_slides_old;
        }else if(fontTypes.contains(ext)){
            return R.drawable.ic_font;
        }else if(pdf.equals(ext)){
            return R.drawable.ic_pdf;
        }else{
            return R.drawable.ic_file_general;
        }
    }

